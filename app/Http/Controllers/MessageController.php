    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $models        = Message::where('id', $id)->isActive()->isForYou()->orderBy('id','desc')->first();
        if(!$models) {
            abort(404);
        }

        $log = new MessageLog();
        $log->user_id = Auth::user()->id;
        $log->message_id = $models->id;
        $log->save();

        return view('admin.message.show', compact('models'));
    }

